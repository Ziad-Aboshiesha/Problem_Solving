    public class Solution
    {
        public int LongestSubarray(int[] nums)
        {
            int max = 0;
            int count = 0;
            int index = 0;
            
            while (index < nums.Length) 
            {
                if (nums[index] > max)
                {
                    max = nums[index];
                    count = 1;
                    index++;
                    while (index < nums.Length && nums[index] == max)
                    {
                        index++;
                        count++;

                    }
                    index--;
                }
                else if (nums[index] == max) 
                {
                    int temp_count = 1;
                    index++;
                    while (index < nums.Length && nums[index] == max)
                    {
                        index++;
                        temp_count++;

                    }
                    count= Math.Max(count, temp_count);
                    index--;
                }
                    index++;
            }
          
            return count;
        }
        
    }
